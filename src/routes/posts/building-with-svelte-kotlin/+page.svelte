<h1>Building Projects with Svelte and Kotlin</h1>

<p><em>April 2026</em></p>

<hr />

<p>
  When it comes to building modern applications, choosing the right stack is critical. Over the last few months, I've found that Svelte and Kotlin make a highly productive and enjoyable combination.
</p>

<h2>The Frontend: Svelte</h2>
<p>
  Svelte has completely changed how I think about frontend development. Instead of shipping a heavy runtime to the browser, Svelte compiles my code down to highly optimized vanilla JavaScript. It's incredibly fast, and the developer experience is fantastic. Building interactive UI components feels much more natural without having to worry about complex state management or virtual DOM diffing.
</p>

<h2>The Backend: Kotlin</h2>
<p>
  On the backend, Kotlin brings the robustness of the JVM ecosystem while offering a modern, concise syntax. Combined with databases like PostgreSQL for relational data and SQLite for simpler or local scenarios, Kotlin allows me to write reliable and type-safe backend services.
</p>

<h2>Why this Combo?</h2>
<p>
  Using Svelte for a responsive, reactive frontend and Kotlin for a robust, scalable backend gives me the best of both worlds. It allows me to iterate quickly on UI changes while trusting the backend to handle complex logic and data safely.
</p>


<h1>Journey in Competitive Programming</h1>

<p><em>April 2026</em></p>

<hr />

<p>
  Competitive programming has been a significant part of my journey as a software engineer. It has helped me develop a strong foundation in data structures and algorithms, which is essential for solving complex real-world problems efficiently.
</p>

<h2>The Beginning</h2>
<p>
  I started by solving beginner-friendly problems on LeetCode. This helped me get comfortable with basic concepts like arrays, strings, and hash maps. To date, I've solved over 60 easy problems on LeetCode, which really solidified my fundamentals.
</p>

<h2>Stepping up to Codeforces</h2>
<p>
  Once I was comfortable with the basics, I decided to take on bigger challenges on Codeforces. The problems there often require more mathematical thinking and deeper algorithmic insights. It was tough at first, but with persistence, I've managed to solve over 200 problems on the platform.
</p>

<h2>Takeaways</h2>
<p>
  The biggest takeaway from this journey is that persistence pays off. Learning C++ was incredibly beneficial here, as it's fast and has a rich standard template library (STL). Competitive programming isn't just about writing code; it's about optimizing solutions and thinking of edge cases, skills that translate perfectly to software development.
</p>

